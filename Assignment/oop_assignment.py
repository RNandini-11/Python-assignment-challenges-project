    def insert(self, data):
        new_node = Node(data)
        if self.head is None:
            self.head = new_node
        else:
            temp = self.head
            while temp.next:
                temp = temp.next
            temp.next = new_node
        print(f"Inserted: {data}")

    def delete(self, key):
        temp = self.head
        if temp and temp.data == key:
            self.head = temp.next
            print(f"Deleted: {key}")
            return
        prev = None
        while temp and temp.data != key:
            prev = temp
            temp = temp.next
        if temp is None:
            print("Value not found!")
            return
        prev.next = temp.next
        print(f"Deleted: {key}")

    def display(self):
        temp = self.head
        if temp is None:
            print("Linked List is empty")
            return
        print("Linked List:", end=" ")
        while temp:
            print(temp.data, end=" -> ")
            temp = temp.next
