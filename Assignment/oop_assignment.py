class Node:
    def __init__(self, key):
        self.key = key
        self.left = None
        self.right = None

class BinarySearchTree:
    def __init__(self):
        self.root = None
    def insert(self, key):
        if self.root is None:
            self.root = Node(key)     # First node becomes root
        else:
            current = self.root
            while True:
                if key < current.key:
                    if current.left is None:
                        current.left = Node(key)  # Insert on left
                        break
                    current = current.left
                elif key > current.key:
                    if current.right is None:
                        current.right = Node(key)  # Insert on right
                        break
                    current = current.right
                else:
                    break                    # key already exists, do nothing

    def search(self, key):
        current = self.root
        while current:
            if key == current.key:
                return True
            elif key < current.key:
                current = current.left
            else:
                current = current.right
        return False

